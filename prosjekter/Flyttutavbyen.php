nyte kafélivet eller det er muligheten til å la barna leke fritt. Men
hva om boligen faktisk så ut som det ”vi ser etter”. Ser du etter
barnevennlige boliger? Så finn en så barnevennlig bolig at den
ser ut som en leke! Ved slik å kombinere kjente ”byggetekniske
