<p>Block Watne er en av Norges største boligbyggere og tilbyr
boliger over hele Sør-Norge, fra Grimstad helt nord til Steinkjer.
De fleste boligene er plassert noe utenfor bysentraene – hvor det
er mer plass og bedre luft, men hvor det fremdeles er kort vei til
alt du behøver i hverdagen. Dette er attraktivt for veldig mange
byboere – ikke minst barnefamilier – men ikke alle er klar over
hva du faktisk kan få for pengene. Block Watne ønsket derfor
våren 2016 å oppfordre til å flytte ut av den skitne, trange byen
til et bedre liv utenfor sentrum. Målsettingen var salg av boliger
samt endre holdningen til at det er bra å bo utenfor byen.
I et marked hvor Block Watnes konkurrenter bruker store
summer på markedsføring, ble det også avgjørende å kunne å
gjøre noe annerledes, utenfor den ”vanlige” boligannonsens
form og stil.
	</p>



